export interface TaxCreditOrDeduction {
  title: string;
  description: string;
  url: string;
  type: "rate" | "flat_amount" | "per_transaction";
  class: "credit" | "deduction";
  // A tax credit is applied against your tax liability. A tax deduction is applied against your taxable income.
  amount: number;
}

export interface AdditionalTax {
  threshold: string;
  name: string;
  rate: number;
  title: string;
  description: string;
  url: string;
}

export interface TaxationData {
  code: string;
  slug: string;
  /** Indicates if there is no income tax system  */
  zero_tax_system: boolean;
  /**
   * Defines how worldwide income is taxed for residents and non-residents.
   * Possible values: 'worldwide_for_residents_local_for_non_residents' (default: false)
   */
  worldwide_for_residents_local_for_non_residents?: boolean;
  /** Defines how citizenship affects taxation (default: false) */
  citizenship_tax_system?: boolean;
  /** Defines how residence affects taxation (default: false) */
  territorial_tax_system?: boolean;
  /** Defines if worldwide income is taxed (default: true) */
  worldwide_tax_system?: boolean;
  /** Details on non-resident taxation (optional) */
  non_resident_taxation?: boolean;
  /** Link to the government tax agency website (optional) */
  government_tax_agency_link?: string;
  /** Name of the government tax agency (optional) */
  government_tax_agency?: string;
  /** Array of taxation regimes for different provinces (optional) */
  province_level_taxation_regimes?: string[];
  /** Defines if non-domiciled individuals are taxed differently (default: true) */
  non_domiciled_taxation?: boolean;
  /** Wealth tax threshold (optional) */
  wealth_tax_threshold?: number;
  /** Array of tax incentives (optional) */
  incentives?: string[];
  /** Array of tax deductions (optional) */
  deductions?: TaxCreditOrDeduction[];
  /** Defines if personal income tax exists (default: true) */
  personal_income_tax?: boolean;
  /** Maximum personal income tax rate */
  personal_income_tax_limit: number | undefined;
  /** Maximum capital gains tax rate */
  capital_percentage_limit: number | undefined;
  /** Array of taxable assets (optional) */
  taxable_assets?: string[];
  /** Array of notes (optional) */
  notes: string[];
  /** Defines if capital gains tax exists (default: true) */
  capital_gains_tax?: boolean;
  /** Defines if wealth tax exists (default: false) */
  wealth_tax?: boolean;
  /** Wealth tax percentage (default: 0) */
  wealth_tax_percentage?: number;
  /** Defines if inheritance tax exists (default: false) */
  inheritance_tax?: boolean;
  /** Inheritance tax percentage (default: 0) */
  inheritance_tax_percentage?: number;
  /** Number of days to be considered a resident for tax purposes */
  residence_threshold?: number | undefined | null;

  value_added_tax_rate?: number | undefined;

  resident_withholding_tax_rate: [
    number | undefined | null,
    number | undefined | null,
    number | undefined | null
  ];
  non_resident_withholding_tax_rate: [
    number | undefined | null,
    number | undefined | null,
    number | undefined | null
  ];

  corporate_tax_rate?: number | undefined;

  corporate_capital_gains_rate?: number | undefined;

  gift_tax_rate?: number | undefined;
  national_medical_tax_rate?: number | undefined;
  totalization_agreement_parties: string[];
  additional_taxes: AdditionalTax[];
}

